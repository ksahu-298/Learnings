#Expressions are constructs made up of values, variables, operators, and calls to functions that Python interprets and computes to produce other values. Statements, on the other hand, are instructions that the Python interpreter can execute. They do not return a value but perform an action. 
# Example of Expressions
a = 5
b = 10
sum = a + b  # 'a + b' is an expression
print("Sum:", sum)

# Example of Statements
if sum > 10:  # 'if' statement
    print("Sum is greater than 10")  # 'print' statement
    
# we can also use semi-colon (;) to use more then one statement on a single line

name=input("Enter your name : ") ; print("Your name is", name) 