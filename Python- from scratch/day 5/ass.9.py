# create two sets of numbers and perform union and intersection b/w them -

numSet1 = {1, 2, 3, 4, 5}
numSet2 = {4, 5, 6, 7, 8}

union_result = numSet1.union(numSet2)
intersection_result = numSet1.intersection(numSet2)

print("Union of numSet1 and numSet2:", union_result)
print("Intersection of numSet1 and numSet2:", intersection_result)
